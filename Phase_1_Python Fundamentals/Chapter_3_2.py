
#Exploring Python data types

# Numeric types
integer_num = 42
float_num = 3.14159
complex_num = 3 + 4j

# Text type
text_string = "Hello, Python!"

# Boolean type
is_true = True
is_false = False

# Sequence types
my_list = [1, 2, 3, "four", 5.0]
my_tuple = (1, 2, 3)
my_range = range(5)

# Mapping type
my_dict = {"name": "Python", "version": 3.11, "type": "language"}

# Set type
my_set = {1, 2, 3, 4, 5}

# Display types and values
print(f"Integer: {integer_num} (type: {type(integer_num)})")
print(f"Float: {float_num} (type: {type(float_num)})")
print(f"String: {text_string} (type: {type(text_string)})")
print(f"List: {my_list} (type: {type(my_list)})")
print(f"Tuple: {my_tuple} (type: {type(my_tuple)})")
print(f"Range: {my_range} (type: {type(my_range)})")
print(f"Dictionary: {my_dict} (type: {type(my_dict)})")
